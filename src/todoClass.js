export class Todo {
    constructor(title, project, description, icon, dueDate, priority, checklist, id) {
        this.title = title,
        this.project = project,
        this.description = description,
        this.icon = icon,
        this.dueDate = dueDate,
        this.priority = priority,
        this.checklist = checklist,
        this.id = id;
    }
}